export function VendorAvatar({
  monogram,
  providerKey,
}: {
  monogram: string;
  providerKey?: string;
}): JSX.Element {
  const logo = providerKey !== undefined ? VENDOR_LOGO[providerKey] : undefined;
  return (
    <span className="vendor-avatar" aria-hidden="true">
      {logo ?? monogram}
    </span>
  );
}
